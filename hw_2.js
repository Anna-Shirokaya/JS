if (!(/[A-ZА-Я]/.test(stroka))){
    console.log("Должна быть хотя бы одна буква в верхнем регистре")
}
//проверка, что строка содержит цифру
if (!(/[1-9]/.test(stroka))){
    console.log("Строка должна содержать хотя бы одну цифру")
}
//проверка, что строка содержит @
